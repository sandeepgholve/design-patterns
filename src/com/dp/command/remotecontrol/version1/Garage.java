public class Garage {
    public void up() {
        System.out.println("Garage - Up");
    }

    public void down() {
        System.out.println("Garage - Down");
    }

    public void stop() {
        System.out.println("Garage - Stop");
    }

    public void lightOn() {
        System.out.println("Garage - Light On");
    }

    public void lightOff() {
        System.out.println("Garage - Light Off");
    }

}
